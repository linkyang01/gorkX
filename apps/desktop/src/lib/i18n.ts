/** System-locale driven zh / en strings for gorkX. */

export type Locale = 'zh' | 'en';

function detectLocale(): Locale {
  if (typeof navigator === 'undefined') return 'en';
  const list = [navigator.language, ...(navigator.languages ?? [])].filter(Boolean);
  // Prefer Chinese if any preferred language is zh*
  if (list.some((l) => l.toLowerCase().startsWith('zh'))) return 'zh';
  const lang = list[0] || 'en';
  return lang.toLowerCase().startsWith('zh') ? 'zh' : 'en';
}

